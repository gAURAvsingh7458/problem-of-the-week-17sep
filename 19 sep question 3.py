# Problem 3: Write a function that finds the maximum number in a list without using max().
def find_max_number(numbers):
    if not numbers:
        return None
    max_num = numbers[0]
    for num in numbers:
        if num > max_num:
            max_num = num
    return max_num

my_numbers = [10, 5, 8, 20, 3, 15]
print(find_max_number(my_numbers))
